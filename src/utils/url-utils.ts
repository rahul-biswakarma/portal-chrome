export const isRestrictedUrl = (url: string): boolean => {
  const restrictedPatterns = [
    'chrome://',
    'chrome-extension://',
    'edge://',
    'about:',
    'file://',
    'data:',
    'view-source:',
    'devtools://',
    'chrome-search://',
    'chrome-devtools://',
    'chrome-internal://',
    'chrome-untrusted://',
    'chrome-webstore://',
    'chrome-extension://',
    'chrome-sync://',
    'chrome-settings://',
    'chrome://flags',
    'chrome://version',
    'chrome://components',
    'chrome://downloads',
    'chrome://extensions',
    'chrome://history',
    'chrome://bookmarks',
    'chrome://settings',
    'chrome://help',
    'chrome://net-internals',
    'chrome://system',
    'chrome://webrtc-internals',
    'chrome://media-internals',
    'chrome://quota-internals',
    'chrome://serviceworker-internals',
    'chrome://tracing',
    'chrome://inspect',
    'chrome://process-internals',
    'chrome://site-engagement',
    'chrome://translate-internals',
    'chrome://user-actions',
    'chrome://webui-test',
    'chrome://webrtc-logs',
    'chrome://webrtc-stats',
    'chrome://webrtc-stats-internals',
    'chrome://webrtc-stats-internals-ui',
    'chrome://webrtc-stats-internals-ui-internals',
    'chrome://webrtc-stats-internals-ui-internals-internals',
  ];

  return restrictedPatterns.some(pattern => url.startsWith(pattern));
};
